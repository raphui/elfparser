char *symbol_get_name(unsigned int addr)
{
	int size = sizeof(symbols) / sizeof(struct sym);
	int i = 0;
	char *ret = NULL;

	for (i = 0; i < size; i++) {
		if (symbols[i].addr == addr) {
			ret = symbols[i].name;
			break;
		} 
	}

	return ret;
}

int symbol_get_addr(char *name)
{
	int size = sizeof(symbols) / sizeof(struct sym);
	int i = 0;
	int ret = 0;

	for (i = 0; i < size; i++) {
		if (!strcmp(name, symbols[i].name)) {
			ret = symbols[i].addr;
			break;
		}
	}

	return ret;
}
